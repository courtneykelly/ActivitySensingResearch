package research.activitysensingresearch;

/**
 * Model for accelerometer data table
 * Created by Ryan Smick on 11/16/2015.
 */
public class AccelerometerData{

    public double X;
    public double Y;
    public double Z;

    public AccelerometerData(double x, double y, double z){
        X = x;
        Y = y;
        Z = z;
    }
}
